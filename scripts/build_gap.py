def compute_macros(ingredients, ing_by_ref):
    kcal = protein = carbs = fat = 0.0
    for ing in ingredients:
        db = ing_by_ref[ing['ref']]
        f = ing['qty_g'] / 100.0
        mm = db['macros_per_100g']
        p, c, ft = mm['protein_g'], mm['carbs_g'], mm['fat_g']
        protein += p * f
        carbs += c * f
        fat += ft * f
        kcal += (p * 4 + c * 4 + ft * 9) * f
    return {
        'kcal': round(kcal),
        'protein_g': round(protein),
        'carbs_g': round(carbs),
        'fat_g': round(round(fat, 1)),
    }


def finalize(recipe, ing_by_ref):
    for ing in recipe['ingredients']:
        db = ing_by_ref[ing['ref']]
        ing.setdefault('perishable', db['perishable'])
        ing.setdefault('sub', None)
    recipe['macros'] = compute_macros(recipe['ingredients'], ing_by_ref)
    recipe.setdefault('cost_band', max(ing_by_ref[i['ref']]['cost_band'] for i in recipe['ingredients']))
    recipe.setdefault('host', None)
    recipe.setdefault('variants', [])
    if 'spice' not in recipe and recipe['tags'].get('meal') != ['drink']:
        recipe['spice'] = {'level': 0}
    if 'smell' not in recipe:
        recipe['smell'] = 'low'
    return recipe


def I(ref, qty_g, display, sub=None):
    return {'ref': ref, 'qty_g': qty_g, 'display': display, 'sub': sub}


def S(title, text, timer_s=0, cue=''):
    return {'title': title, 'text': text, 'timer_s': timer_s, 'cue': cue}


def R(problem, fix):
    return {'problem': problem, 'fix': fix}


