def main():
    errors = []

    with open(ING_PATH) as f:
        ingredients = json.load(f)
    with open(REC_PATH) as f:
        recipes = json.load(f)

    count_before = len(recipes)
    existing_ids = {r['id'] for r in recipes}
    ing_by_ref = {i['ref']: i for i in ingredients}

    ing_added = 0
    for ing in NEW_INGREDIENTS:
        if ing['ref'] not in ing_by_ref:
            ingredients.append(ing)
            ing_by_ref[ing['ref']] = ing
            ing_added += 1

    if ing_added:
        with open(ING_PATH, 'w') as f:
            json.dump(ingredients, f, indent=2)
            f.write('\n')

    gap = gap_recipes(ing_by_ref)
    recipes_added = []
    recipes_skipped = []

    for rec in gap:
        rid = rec['id']
        if rid in existing_ids:
            recipes_skipped.append(rid)
            continue
        for ing in rec['ingredients']:
            if ing['ref'] not in ing_by_ref:
                errors.append(f"{rid}: missing ingredient ref '{ing['ref']}'")
        recipes.append(rec)
        existing_ids.add(rid)
        recipes_added.append(rid)

    def sort_key(r):
        no = r.get('no')
        return (no if no is not None else 9999, r['id'])

    recipes.sort(key=sort_key)

    with open(REC_PATH, 'w') as f:
        json.dump(recipes, f, indent=2)
        f.write('\n')

    count_after = len(recipes)

    print(f"Ingredients: added {ing_added}, skipped {len(NEW_INGREDIENTS) - ing_added}")
    print(f"Recipes before: {count_before}")
    print(f"Recipes after:  {count_after}")
    print(f"Recipes added:  {len(recipes_added)}")
    print(f"Recipes skipped:{len(recipes_skipped)}")
    if recipes_added:
        print(f"New ids: {', '.join(recipes_added)}")
    if recipes_skipped:
        print(f"Skipped: {', '.join(recipes_skipped)}")
    if errors:
        print(f"ERRORS ({len(errors)}):")
        for e in errors:
            print(f"  {e}")
        sys.exit(1)


if __name__ == '__main__':
    main()
